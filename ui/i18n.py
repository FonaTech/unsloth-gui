def normalize_language(value: str | None) -> str:
    if value in TRANSLATIONS:
        return value
    return DEFAULT_LANGUAGE


def tr(key: str) -> str:
    """Return translated plain string in default language (for initial build)."""
    return ts(key, DEFAULT_LANGUAGE)


def ts(key: str, lang: str = DEFAULT_LANGUAGE) -> str:
    """Return translated plain string for use in choices tuples."""
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE]).get(key, key)


