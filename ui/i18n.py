def get_choices(name: str, lang: str = DEFAULT_LANGUAGE) -> list[tuple[str, str]]:
    """返回指定 choices 组的翻译后列表，用于动态更新 Radio/Dropdown。"""
    return [(ts(k, lang), v) for k, v in CHOICES_DEFS[name]]


# ── Component registry for language switching ──────────────────────
_COMPONENT_REGISTRY: list[tuple] = []


def register_translatable(comp, label_key=None, info_key=None, choices_key=None):
    """Register a Gradio component for dynamic language updates."""
    _COMPONENT_REGISTRY.append((comp, label_key, info_key, choices_key))


def build_language_update(lang: str) -> list:
    """Return list of gr.update() for all registered components."""
    # These layout containers don't support label updates without re-rendering children
    _SKIP_TYPES = (gr.Tab, gr.Accordion)
    updates = []
    for comp, label_key, info_key, choices_key in _COMPONENT_REGISTRY:
        if isinstance(comp, _SKIP_TYPES):
            updates.append(gr.update())
            continue
        kwargs = {}
        if label_key:
            # gr.Button and gr.Markdown use value= not label=
            if isinstance(comp, (gr.Button, gr.Markdown)):
                kwargs["value"] = ts(label_key, lang)
            else:
                kwargs["label"] = ts(label_key, lang)
        if info_key:
            kwargs["info"] = ts(info_key, lang)
        if choices_key:
            kwargs["choices"] = get_choices(choices_key, lang)
        updates.append(gr.update(**kwargs) if kwargs else gr.update())
    return updates


def get_registered_components() -> list:
    """Return list of all registered component refs (for outputs=)."""
    return [comp for comp, *_ in _COMPONENT_REGISTRY]


# ── Current language tracker (for runtime string generation) ──────
_current_lang: str = DEFAULT_LANGUAGE


def get_current_lang() -> str:
    return _current_lang


def set_current_lang(lang: str) -> None:
    global _current_lang
    _current_lang = normalize_language(lang)


def t(key: str) -> str:
    """Translate key using the current runtime language."""
    return ts(key, _current_lang)
